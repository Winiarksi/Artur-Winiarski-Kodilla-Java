package Module_6.Module_6_1;

public class Calkulator {

    private int a;
    private int b;

    public int add(int a, int b){
        return a + b;
    }

    public int subtrack(int a, int b){
        return a - b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }
}
